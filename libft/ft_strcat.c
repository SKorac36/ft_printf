#include "libft.h"

char	*ft_strcat(char *s1, const char *s2)
{
	size_t a;
	size_t b;

	a = ft_strlen(s1);
	b = 0;
	while (s2[b] != '\0')
	{
		s1[a + b] = s2[b];
		b++;
	}
	s1[a + b] = '\0';
	return (s1);
}
